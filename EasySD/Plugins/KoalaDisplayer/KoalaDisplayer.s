; KOALA displayer plugin.
; Runtime contract: AVR preloads the 10001-byte Koala payload to $2000
; and this PRG payload to $C000, then launches $C000.

.enc "screen"

	*=$C000
	JMP MAIN

MAIN
	JSR INIT
	JSR VIEWKOALA
	JMP *

INIT
	CLD
	LDA #$00
	STA $D020
	LDA #$0B
	STA $D021
	JSR INITPC
	JSR DISABLEINTERRUPTS
	JSR KILLCIA
	RTS

INITPC
	LDX #$00
	LDA #$0F
CBL
	STA $D800,X
	STA $D900,X
	STA $DA00,X
	STA $DB00,X
	INX
	BNE CBL
	RTS

KILLCIA
	LDY #$7F
	STY $DC0D
	STY $DD0D
	LDA $DC0D
	LDA $DD0D
	RTS

DISABLEINTERRUPTS
	LDY #$7F
	STY $DC0D
	STY $DD0D
	LDA $DC0D
	LDA $DD0D
	ASL $D019
	LDA #$00
	STA $D01A
	RTS

PICTURE = $2000
BITMAP = PICTURE
VIDEO = PICTURE+$1F40
COLOR = PICTURE+$2328
BACKGROUND = PICTURE+$2710
SCREENRAM = $0400

VIEWKOALA
	JSR FORCEIO

	LDA #$0B
	STA $D011
	LDA #$00
	STA $D020

	LDX #$00
LOOPTRANSFER
	LDA VIDEO,X
	STA SCREENRAM,X
	LDA VIDEO+$100,X
	STA SCREENRAM+$100,X
	LDA VIDEO+$200,X
	STA SCREENRAM+$200,X
	LDA VIDEO+$2E8,X
	STA SCREENRAM+$2E8,X
	LDA COLOR,X
	STA $D800,X
	LDA COLOR+$100,X
	STA $D900,X
	LDA COLOR+$200,X
	STA $DA00,X
	LDA COLOR+$2E8,X
	STA $DAE8,X
	INX
	BNE LOOPTRANSFER

	LDA BACKGROUND
	STA $D021

	LDA $DD00
	AND #$FC
	ORA #$03
	STA $DD00

	LDA #$18
	STA $D018
	LDA #$D8
	STA $D016
	LDA #$3B
	STA $D011
	RTS

FORCEIO
	LDA #$37
	STA $01
	RTS
