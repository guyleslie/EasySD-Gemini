INITTEXT
	.TEXT "IRQHACK64V2 PETSCII PLUGIN"
	.BYTE 0	
	
;-----------------------------------------------
; Plugin-specific label aliases for compatibility
;-----------------------------------------------
SENDSTARTTALKING = STARTTALKING
STARTEDTALKING = TALKINGSTARTED
OPENINGFILEFAILED = OPENINGFAILED
FILECLOSED = CLOSINGSUCCESS
ERRORCLOSINGFILE = CLOSINGFAILED

FORCEIO
	LDA $01
	ORA #$04			; Ensure I/O visible ($D000-$DFFF)
	STA $01
	RTS

SAVESTATE
	LDA $01
	STA SAVED_01
	LDA $DD00
	STA SAVED_DD00
	LDA $D011
	STA SAVED_D011
	LDA $D016
	STA SAVED_D016
	LDA $D018
	STA SAVED_D018
	LDA $D020
	STA SAVED_D020
	LDA $D021
	STA SAVED_D021
	LDA $D022
	STA SAVED_D022
	LDA $D023
	STA SAVED_D023
	RTS

RESTORESTATE
	JSR FORCEIO
	LDA SAVED_01
	STA $01
	LDA SAVED_DD00
	STA $DD00
	LDA SAVED_D018
	STA $D018
	LDA SAVED_D016
	STA $D016
	LDA SAVED_D011
	STA $D011
	LDA SAVED_D020
	STA $D020
	LDA SAVED_D021
	STA $D021
	LDA SAVED_D022
	STA $D022
	LDA SAVED_D023
	STA $D023
	RTS

